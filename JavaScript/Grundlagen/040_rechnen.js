/*
====================================================
    Rechenoperatoren
====================================================

+   plus
-   minus
*   mal
/   geteilt durch
%   Restwert / Modulo
*/

let snacks = 10;
let tiere = 3;

console.log(snacks + tiere);
console.log(snacks - tiere);
console.log(snacks * tiere);
console.log(snacks / tiere);

/*
====================================================
    Modulo %
====================================================

Modulo gibt dir den Rest einer Division.

Beispiel:
10 Snacks auf 3 Tiere verteilen.

Jedes Tier bekommt 3 Snacks.
1 Snack bleibt übrig.

10 % 3 ergibt 1.
*/

console.log(10 % 3);

/*
Das ist super nützlich, um zu prüfen,
ob eine Zahl gerade oder ungerade ist.

Gerade Zahl:
zahl % 2 ergibt 0

Ungerade Zahl:
zahl % 2 ergibt 1
*/

let muenzen = 7;

console.log(muenzen % 2);

